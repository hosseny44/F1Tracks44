package com.example.tracks.Classes;

public class F1Team {
    private String teamId;
    private String teamName;
    private String teamNationality;
    private int constructorsChampionships;
    private int driversChampionships;
    private String firstAppeareance;
    private String url;

    // getters
    public String getTeamId() { return teamId; }
    public String getTeamName() { return teamName; }
    public String getTeamNationality() { return teamNationality; }
    public int getConstructorsChampionships() { return constructorsChampionships; }
    public int getDriversChampionships() { return driversChampionships; }
    public String getFirstAppeareance() { return firstAppeareance; }
    public String getUrl() { return url; }
}